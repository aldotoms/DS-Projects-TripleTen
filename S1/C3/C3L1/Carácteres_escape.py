# Carácteres d eescape
# Los caracteres de escape se designan con una barra invertida seguida de cualquier carácter especial que queramos en nuestro string. 
# Esto nos ofrece una forma de mostrar el string del ejercicio anterior sin usar comillas dobles:

print('Let\'s learn about strings!')

# Además de las comillas simples y dobles, hay otros caracteres de escape. La mayoría de ellos se utilizan para formatear texto, siendo los dos más comunes:
# \n: el carácter de nueva línea, para agregar un salto de línea, haciendo que tu string continúe en la siguiente línea.
# \t: el carácter de tabulación, para agregar tabulación a tu string.
# Aquí hay un ejemplo que usa ambos en el mismo string:

print('La pelicula favorita de Pete es:\t\t la ultima que vio\n:D]')

