# ¡Python nos permite crear strings multilínea! Para hacerlo, envuelve el string en tres comillas simples ’’’ o dobles ”””. 
# Aquí tienes un ejemplo:

shopping_list = """Mi lista de compras:
2 cajas de leche,
cereal,
huevos,
pan.
"""

print(shopping_list)

# El string multilínea se muestra usando print(), de la misma forma en que lo escribimos en nuestro código, 
# incluso sin usar ningún carácter de escape.
# Fin.