# Operaciones aritméticas en Python:

# 1. Suma
# Este operador (+) se utiliza para sumar dos valores. Podemos usarlo de la siguiente manera:
print(1.5 + 10)

# 2. Resta
# El operador de resta (-) proporciona la diferencia entre dos valores.
print(1.5 - 10)

# 3. Multiplicación
# El operador de multiplicación (*) se utiliza para multiplicar dos valores.
print(5 * -3)

# 4. División
# El operador de división (/) se usa para dividir dos valores.
print(15 / 10)

# Módulo
# El operador de módulo (%) nos permite obtener el resto o residuo de la operación de división; es decir, la parte fraccionaria.
# Observa que, al emplear este operador, la operación resultante tendrá el mismo signo que el divisor.
print(15 % 10)
print(15 % -10)
print(-15 % 10)

# Exponenciación
# El operador de exponenciación (**") se utiliza para aumentar el valor de un número a la potencia de otro.
print(10 ** 3)

# Division entera
# La división entera (//) se usa para dividir dos números y redondear el resultado a la baja al entero más cercano.
print(15 // 10)

# Orden de las Operaciones
# La prioridad suele seguir el mismo orden de operaciones que se utiliza en matemáticas, es decir:
# 1. Exponentes, 2. Multiplicaciones y Divisiones, 3. Sumas y Restasa
# Pero también puedes anular esto utilizando paréntesis para agrupar las partes que deseas que tengan prioridad.
print( (2 ** 3 + 12 / 2) / (5 * 4 - 13) )
