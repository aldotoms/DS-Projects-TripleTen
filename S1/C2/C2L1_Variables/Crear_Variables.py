#En los lenguajes de programación, las variables desempeñan la función de contenedores de otros valores (texto, números, etc.)
#El uso de variables nos permite guardar esos valores para poder referirnos a ellos más adelante.
#En una variable, puedes guardas casi cualquier cosa: fechas, oraciones, números, resultados de cálculos, listas y mucho más.

current_champion = 'Novak Djokovich'
